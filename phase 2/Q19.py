#القواميس (dictionaries) هي نوع من أنواع البيانات في بايثون الذي يتيح لك إنشاء قوائم من البيانات المتنوعة. يمكنك إنشاء قاموس باستخدام الدالة dict() أو باستخدام قوسين {}.

person = {"اسم": "محمد", "عمر": 25, "جنس": "ذكر"}
print(person["اسم"])  # Output: محمد
print(person["عمر"])  # Output: 25
print(person["جنس"])  # Output: ذكر