#في بايثون, الاستثناءات (exceptions) هي حالات غير متوقعة يمكن أن تحدث في البرنامج. يمكن استخدام الاستثناءات للتعامل مع حالات مثل تحليل القيم غير الصحيحة أو الوصول إلى الملفات غير موجودة.

#يمكنك استخدام الاستثناءات باستخدام عبارة try و `except

def القسمة(x, y):
    try:
        return x / y
    except ZeroDivisionError:
        return "لا يمكن تقسيم على الصفر!"

print(القسمة(5, 2))  # Output: 2.5
print(القسمة(5, 0))  # Output: لا يمكن تقسيم على الصفر!